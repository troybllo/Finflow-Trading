package com.finflow.portfolio.domain;

/**
 * Enumeration representing the different types of assets that can be held in a portfolio.
 */
public enum AssetType {
    /**
     * Stock/Equity instruments
     */
    STOCK,

    /**
     * Cryptocurrency
     */
    CRYPTO,

    /**
     * Foreign Exchange
     */
    FOREX,

    /**
     * Options contracts
     */
    OPTION,

    /**
     * Futures contracts
     */
    FUTURE
}
